package soporte;

public class AplicacionesSoportadas {
    private String SAP;
    private String TANGO;
    private String AppX;

    
    public String getSAP() {
        return SAP;
    }

    public void setSAP(String SAP) {
        this.SAP = SAP;
    }

    public String getTANGO() {
        return TANGO;
    }

    public void setTANGO(String TANGO) {
        this.TANGO = TANGO;
    }

    public String getAppX() {
        return AppX;
    }

    public void setAppX(String AppX) {
        this.AppX = AppX;
    }
}
